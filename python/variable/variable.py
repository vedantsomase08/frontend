# Variables are containers for storing data value.

# Python has no command for declaring a varibale.
# A variable is created the moment you first assign a value to it.

x = 5
y = "Python"
print(x)
print(y)


# Variable do not need to be declared with any particular type, and can even change type after they have been set.

a = 4
a = "Salt"
print(a)



# Python allows you to assign values to multiple variables in one line:

k, l,m = "Orange", "Banana", "Cherry"
print(k)
print(l)
print(m)


# And you can assign the same value to multiple variables in one line:

x = y = z = "Orange"
print(x)
print(y)
print(z)

# In the print() function, you output multiple variables, separated by a comma:

x = "Python"
y = "is"
z = "awesome"
print(x, y, z)