# Python Bitwise operators act on bits and perform bit-by-bit operations. These are used to operate on binary numbers.

a = 10
b = 4

print(a & b)        #AND
print(a | b)        #OR  
print(~a)           #NOT
print(a ^ b)        #XOR
print(a >> 2)       #Signed right shift
print(a << 2)       #Zero fill left shift